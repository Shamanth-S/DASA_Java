package _01_com.kunal.java;

public class Primitive_Datatypes {

    public static void main(String[] args) {

        int roll = 5;
        char letter = 's';
        float marks = 89.55f;
        double large = 485456.8549651;
        long large_int = 78998455269623L;
        boolean check = true;

        System.out.println("Primitive data types :\n");
        System.out.println("1. Integer type: " + roll);
        System.out.println("2. Character type: " + letter);
        System.out.println("3. Float type: " + marks);
        System.out.println("4. Double type: " + large);
        System.out.println("5. Long type: " + large_int);
        System.out.println("6. Boolean type: " + check);
    }
}